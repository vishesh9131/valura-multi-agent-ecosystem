NARRATIVE_SYSTEM = """\
You are a portfolio health assistant for novice investors. Speak plainly,
no jargon without context, four sentences max. Open by saying what you see
overall, then call out the single most important thing the user should know,
then close with one practical next step. Do not list every metric — the
structured payload covers that. Do NOT include the regulatory disclaimer.

""" + MODEL_INJECTION_GUARD


def _build_narrative_user_msg(query: str, structured: dict[str, Any]) -> str:
    return (
        f"USER QUERY: {query}\n\n"
        f"STRUCTURED ASSESSMENT (do not repeat verbatim, summarise it):\n{structured}\n"
    )


class PortfolioHealthAgent:
    name = "portfolio_health"

    def _with_readable_message(self, structured: dict[str, Any]) -> dict[str, Any]:
        out = dict(structured)
        out["message"] = format_portfolio_assessment_plain(structured)
        return out

    async def run(
        self,
        *,
        query: str,
        user_context: dict[str, Any],
        classification: dict[str, Any],
        llm: LLMClient | None = None,
        conversation_history: list[dict[str, str]] | None = None,
    ) -> AsyncIterator[dict[str, Any]]:
        try:
            structured = assess(user_context)
        except Exception as exc:
            logger.exception("Portfolio Health computation failed")
            yield {"type": "data", "delta": "I couldn't fully read your portfolio just now. "}
            yield {
                "type": "structured",
                "payload": {
                    "agent": self.name,
                    "error": "compute_failed",
                    "message": str(exc),
                    "disclaimer": DISCLAIMER,
                },
            }
            return

        # In tests we pass a plain callable as `llm` (the classifier-style
        # fake). It cannot stream, so we treat anything without `stream_text`
        # as "no LLM available" and fall back to the deterministic intro.
        client = llm if (llm is not None and hasattr(llm, "stream_text")) else _maybe_default_llm()
        if client is not None and not hasattr(client, "stream_text"):
            client = None

        if client is None:
            # No LLM available — fall back to a deterministic intro built
            # from the structured payload. Streaming still works, just without
            # the LLM polish.
            intro = _deterministic_intro(structured)
            for chunk in _split_for_stream(intro):
                yield {"type": "data", "delta": chunk}
            yield {"type": "structured", "payload": self._with_readable_message(structured)}
            return

        hist = list(conversation_history or [])
        if hist and hist[-1].get("role") == "user":
            hist = hist[:-1]
        messages = assemble_messages(
            system=NARRATIVE_SYSTEM,
            history=hist,
            user=_build_narrative_user_msg(query, structured),
        )
        try:
            async for piece in client.stream_text(messages, temperature=0.3, max_tokens=300):
                yield {"type": "data", "delta": piece}
        except LLMError as exc:
            logger.warning("Narrative LLM stream failed: %s", exc)
            intro = _deterministic_intro(structured)
            for chunk in _split_for_stream(intro):
                yield {"type": "data", "delta": chunk}

        yield {"type": "structured", "payload": self._with_readable_message(structured)}


def _maybe_default_llm() -> LLMClient | None:
    try:
        return get_llm_client()
    except Exception:
        return None


def _split_for_stream(text: str, *, chunk_size: int = 32) -> list[str]:
    return [text[i : i + chunk_size] for i in range(0, len(text), chunk_size)]


def _deterministic_intro(structured: dict[str, Any]) -> str:
    if structured.get("mode") == "build":
        return structured.get("summary", "")

    conc = structured["concentration_risk"]
    perf = structured["performance"]
    bench = structured["benchmark_comparison"]

    parts = []
    if conc.get("flag") == "high":
        parts.append(
            f"Your portfolio is heavily concentrated in {conc['top_holding']} "
            f"({conc['top_position_pct']}% of total). "
        )
    elif conc.get("flag") == "warning":
        parts.append(
            f"Your largest position is {conc['top_holding']} "
            f"at {conc['top_position_pct']}% — keep an eye on it. "
        )
    if perf.get("total_return_pct") is not None:
        parts.append(f"Trailing one-year return is {perf['total_return_pct']}%. ")
    if bench.get("alpha_pct") is not None and bench.get("benchmark"):
        sign = "ahead of" if bench["alpha_pct"] >= 0 else "behind"
        parts.append(
            f"That puts you {abs(bench['alpha_pct']):.1f}% {sign} {bench['benchmark']}."
        )
    if not parts:
        parts.append("Portfolio looks balanced — see the structured payload below for the numbers.")
    return "".join(parts).strip() or "Portfolio summary attached."
